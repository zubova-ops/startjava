package com.startjava.lesson_1.base;

public class VariableNamesTheme {
    public static void main(String[] args) {
        System.out.println("1.Разные переменные");
        // Цифра 
        int number = 5;
            System.out.println("Цифра" + " " + number);

        // Сумма 
        int sum = 21;
            System.out.println("Сумма чисел = " + sum);

        // Произведение цифр
        int multiplication = 50;
            System.out.println("Произведение цифр= " + multiplication);

        // Максимальное число
        int maxNum = 8;
            System.out.println("Максимальное число " + maxNum);

        // Количество десятков
        int tens = 2;
            System.out.println("Количество десятков = " + tens);

        // Вес собаки
        int dogWeight = 10;
            System.out.println("Вес собаки = " + dogWeight + " " + "кг");

        // Исходное число
        int initNumber = 1;
            System.out.println("Исходное число = " + initNumber);

        // Процент по вкладу
        int depositPercent = 3;
            System.out.println("Процент по вкладу = " + depositPercent);

        // Переменная хранит символ &
        char sym = '&';
            System.out.println(sym);

        // Код ошибки
        int errorCode = 228;
            System.out.println("Код ошибки = " + errorCode);

        // Тип сообщения
        String messageType = "Команда";
            System.out.println(messageType);

        // Число нулей
        int numberOfZeros = 3;
            System.out.println("Число нулей = " + numberOfZeros);

        // Уникальное число
        int uniqueNumber = 5;
            System.out.println("Уникальное число = " + uniqueNumber);

        // Случайное число
        int randomNumber = 2;
            System.out.println("Случайное число = " + randomNumber);

        // Математическое выражение
        int a = 2;
        int b = 3;
        int sumNum = (a + b);
            System.out.println(sumNum);

        // Счёт в игре
        int team1 = 5;
        int team2 = 4;
            System.out.println(team1 + ":" + team2);

        // Максимальная длина
        int maxLength = 20;
            System.out.println(maxLength + " " + "см");

        // Пункт меню
        String menuItem = "Команда";
            System.out.println(menuItem);

        // Стоимость кофе
        int coffeePrice = 300;
            System.out.println(coffeePrice +  " " + "рублей");

        // Дата начала
        String birthDay = "20 ноября 2020 год";
            System.out.println(birthDay);

        // Окончание диапазона
        int endOfRange = 100;
            System.out.println(endOfRange);

        // Полное имя работника месяца
        String fullName = "Зубова Миссандея Станиславовна";
            System.out.println(fullName);

        // Заголовок электронной книги
        String ebookTitle = "Гарри Потер и кубок огня";
            System.out.println(ebookTitle);

        // Размер
        int size = 44;
            System.out.println("Размер = " + size);

        // Вместимость
        int capacity = 3;
            System.out.println("Вместимость = " + capacity + " " + "литра");

        // Счётчик
        String counter = "прибор для подсчёта";
            System.out.println("Счётчик - это" + counter);

        // Путь до файла
        String filePath = "C:\\Users\\Дарья";
            System.out.println(filePath);

        // Количество чисел в каждой сроке
        int amountNumbersInEveryLine = 5;
            System.out.println(amountNumbersInEveryLine);
            System.out.println("                      ");



        System.out.println("2. Boolean - переменные");
        // Сотни равны?
        boolean isEqualHundreds = true;
            System.out.println(isEqualHundreds);

        // Компьютер включен?
        boolean isComputerOn = true;
            System.out.println(isComputerOn);

        // Есть равные цифры?
        boolean hasEqualNumbers = false;
            System.out.println(hasEqualNumbers);

        // Создано?
        boolean produced = true;
            System.out.println(produced);

        // Пустое?
        boolean vacant = true;
            System.out.println(vacant);

        // Активное?
        boolean active = true;
            System.out.println(active);

        // Новое
        boolean isNew = true;
            System.out.println(isNew);

        // Электронная почта действительна?
        boolean isValidEmail = true;
            System.out.println(isValidEmail); 

        // Имеются уникальные строки?
        boolean hasUniqueStrings = true;
            System.out.println(hasUniqueStrings);
            System.out.println("                      ");



        System.out.println("3.Аббревиатуры");
        // Старый universally unique identifier
        String uuid = "стандарт идентификации, используемый в создании программного обеспечения";
            System.out.println(uuid);

        // Производитель оперативной памяти
        String dram = "Крупнейшим производителем модулей оперативной памяти остается компания Kingston Technology";
            System.out.println(dram);

        // Ёмкость жесткого диска
        int hdd = 3;
            System.out.println(hdd + "ТБ");

        // Протокол передачи гипертекста
        String http = "Протокол передачи гипертекста";
            System.out.println(http);

        // uniform resource locator
        String url = "Is the address of a unique resource on the internet";
            System.out.println(url);

        // Новый идентификатор клиента
        String guid = "Уникальный идентификатор клиента";
            System.out.println(guid);

        // American standard code for information interchange
        String ascii = "American standard code for information interchange";
            System.out.println(ascii);  
    }
}
